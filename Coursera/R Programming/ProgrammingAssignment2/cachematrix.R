## This function receives a matrix.  It is assumed that solve() works on
##   this matrix
## This function generates a list with four elements.  The elements named
##   set, get, setsolve, getsolve
makeCacheMatrix <- function(x = matrix()) {
    s <- NULL
    set <- function(y) {
        x <<- y
        s <<- NULL
    }
    get <- function() x
    setsolve <- function(solve) s <<- solve
    getsolve <- function() s
    list(set = set, get = get,
         setsolve = setsolve,
         getsolve = getsolve)
}

## This function receives a list generated by makeCacheMatrix, applies
## the solve function and stores the results in cache.  Thereafter when this
## function is called it retrieves the results from cache rather than re-
## calculating.
cacheSolve <- function(x, ...) {
    s <- x$getsolve()
    if(!is.null(s)) {
        message("getting cached data")
        return(s)
    }
    data <- x$get()
    s <- solve(data, ...)
    x$setsolve(s)
    s
}



